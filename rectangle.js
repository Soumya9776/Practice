function rectangleSquare(value,asses){
    let length = value
    let bredth = asses
    let area = length * bredth
    console.log("area of rectangular " + area)
   
}
rectangleSquare(9,5);

function rectanglePerimeter(value,asses){
    let length=value
    let bredth=asses
    let perimeter=2*(length+bredth)
    console.log("perimeter of rectangle "+perimeter)
}
rectanglePerimeter(8,6)