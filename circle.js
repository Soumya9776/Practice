"use strict";
function circleArea(value,pii){
     let radius=value;
    let pi=pii;
    let area=pi*radius*radius;
    console.log("area of circle "+ area);
}
circleArea(8,3.14);
function circleperimeter(value,pii){
let radius=value;
let pi=pii;
let perimeter=2*pi*radius;
console.log("perimeter of circle "+perimeter);
}
circleperimeter(7,3.14);